#Tuples
fruits = ("apple", "orange", "mango", "watermelon")
print(fruits)

print(fruits[1])

#imutability
#fruits[1] = "Peach" this list mutability doesn't work in tuples

print(fruits[1:3]) #tuples slicing

print(fruits[-1]) #negative index

#as comparing to lists, tuples works faster and data cannot be changed